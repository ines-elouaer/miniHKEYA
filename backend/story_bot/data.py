def get_story(theme: str, level: int):
    """
    Retourne une histoire pour un thème et un niveau donnés.
    Si rien n'est trouvé, retourne None.
    """
    key = (theme, level)
    stories_for_key = STORIES.get(key)
    if not stories_for_key:
        return None
    return stories_for_key[0]
